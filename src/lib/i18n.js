/**
 * Get translation for a key with optional variable interpolation.
 * e.g. t('welcomeUser', { name: 'Alice' }, 'hi-IN') => 'स्वागत है, Alice'
 */
export function t(key, vars = {}, lang = 'en-US') {
  const dict = translations[lang] || translations['en-US'];
  let str = dict[key] ?? translations['en-US'][key] ?? key;
  Object.entries(vars).forEach(([k, v]) => {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
  });
  return str;
}

